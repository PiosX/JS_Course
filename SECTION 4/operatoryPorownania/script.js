console.log(10 == '10');
console.log(10 === '10');

console.log('10' === '10');

// == porownuje zawartosc
// === porownuje zawartosc oraz typ danych

console.log('------');

console.log(10 != '10');
console.log(10 !== '10');
console.log('10' !== '10');

// != sprawdza czy czy lewa zawartosc jest rozna od prawej
// !== sprawdza czy lewa zawartosc jest rowna od prawej i czy ma rozny typ

console.log('------');

console.log(10>5);
console.log(10<5);
console.log(10<=10);
console.log(10>=10);